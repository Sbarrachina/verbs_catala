let currentSentenceIndex = 0;

function showPronounGame() {
    document.getElementById('pronoun-game').classList.remove('hidden');
    loadPronounSentence();
}

function closePronounGame() {
    document.getElementById('pronoun-game').classList.add('hidden');
}

function loadPronounSentence() {
    const sentence = pronounSentences[currentSentenceIndex];
    document.getElementById('pronoun-sentence').innerText = sentence.sentence;

    const optionsContainer = document.getElementById('pronoun-options');
    optionsContainer.innerHTML = '';

    sentence.options.forEach(option => {
        const button = document.createElement('button');
        button.innerText = option;
        button.className = 'option-btn';
        button.onclick = () => checkPronounAnswer(option);
        optionsContainer.appendChild(button);
    });
}

function checkPronounAnswer(selectedOption) {
    const resultElement = document.getElementById('pronoun-result');
    const correctOption = pronounSentences[currentSentenceIndex].correct;

    if (selectedOption === correctOption) {
        resultElement.innerText = 'Correcte!';
        resultElement.style.color = 'green';
    } else {
        resultElement.innerText = `Incorrecte. La resposta correcta és: ${correctOption}`;
        resultElement.style.color = 'red';
    }
}

function nextPronoun() {
    currentSentenceIndex = (currentSentenceIndex + 1) % pronounSentences.length;
    loadPronounSentence();
    document.getElementById('pronoun-result').innerText = '';
}

document.getElementById('next-button').onclick = nextPronoun;

